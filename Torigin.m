function T0 = 	Torigin(x,y,z,mu,w)
%求解消除标称加速度的T0 输入为x0,y0,z0,mu,w输出为T0
T0 = zeros(1,3);
T0(1) = (mu*x)/(x^2 + y^2 + z^2)^(3/2)-w^2*x;
T0(2) = (mu*y)/(x^2 + y^2 + z^2)^(3/2)-w^2*y;
% T0(1) = (mu*x)/(x^2 + y^2 + z^2)^(3/2);
% T0(2) = (mu*y)/(x^2 + y^2 + z^2)^(3/2);
T0(3) = (mu*z)/(x^2 + y^2 + z^2)^(3/2);
end

